	private static double zdt4_h(double f, double g) {
		return 1.0 - Math.sqrt(f / g);
	}

	private static double zdt4_g(double[] x) {
		double result = 91;

		for(int i = 1; i < 10; ++i) {
			result += Math.pow(x[i], 2) - 10 * Math.cos(4 * Math.PI * x[i]);
		}

		return result;
	}

	public static double zdt4_f(double[] x) {
		return zdt4_g(x) * zdt4_h(x[0], zdt4_g(x));
	}

